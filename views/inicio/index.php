		<p>En programación, cuando hablamos de <strong>MVC</strong> nos referimos a un patrón de diseño que organiza 
			el código en tres partes bien diferenciadas: <strong>Modelo</strong>, <strong>Vista</strong> y <strong>Controlador</strong>. 
			El Modelo es el encargado de gestionar la lógica de datos y de cómo interactuar con la base de datos. La Vista se encarga 
			de mostrar la información de forma amigable al usuario, y el Controlador maneja las solicitudes que vienen del usuario, 
			interactuando tanto con el Modelo como con la Vista. Este enfoque hace que el código sea más limpio, fácil de mantener 
			y mejorar con el tiempo.</p>

		<p>Por otro lado, <strong>PDO</strong> significa <strong>PHP Data Objects</strong>, y es una forma de acceder a bases de 
			datos de manera segura en PHP. Usando PDO, podemos conectarnos a diferentes tipos de bases de datos sin tener 
			que escribir código específico para cada una, lo que facilita la portabilidad de nuestra aplicación. Finalmente, 
			cuando hablamos de <strong>POO</strong> o <strong>Programación Orientada a Objetos</strong>, estamos hablando de una 
			manera de organizar el código que imita el mundo real. Las "clases" y "objetos" son como plantillas y entidades que 
			nos permiten crear y manipular elementos de forma más eficiente, agrupando funciones y datos relacionados.</p>

